       <p>
         Valle de Bravo es una oportunidad para disfrutar las atracciones de los grandes clubes de playa sin tener que salir del centro del país, pues se ubica en el norte del multifacético Estado de México, a poco más de 150 kilómetros de la capital del país. Fundado en 1550 por frailes franciscanos en lo alto de las montañas, su belleza colonial se ha visto coronada con el portentoso lago, que en conjunto brinda una gran armonía a la vista y al espíritu.

         Rodeado de un clima predominantemente templado, el conocido cuerpo de agua vallesano es en realidad una presa llamada originalmente Miguel Alemán, que fue construida en 1947 como parte de la Región Hidrológica Río Balsas. 


